package org.operators.arithmetic.double

fun main() {
    println("Numbers: 7 + 2: ${7.0 + 2.0}"); // 9.0
    println("Numbers: 7 - 2: ${7.0 - 2.0}"); // 5.0
    println("Numbers: 7 * 2: ${7.0 * 2.0}"); // 14.0
    println("Numbers: 7 / 2: ${7.0 / 2.0}"); // 3.5
    println("Numbers: 7 % 2: ${7.0 % 2.0}"); // 1.0
}
