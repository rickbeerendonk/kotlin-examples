var a = 7.0
println("a: ${a}")  // 7.0

a = 2.0
println("Double: a = 2.0: ${a}") // 2.0

a = 7.0
a += 2.0
println("Double: a += 2.0: ${a}") // 9.0

a = 7.0
a -= 2.0
println("Double: a -= 2.0: ${a}") // 5.0

a = 7.0
a *= 2.0
println("Double: a *= 2.0: ${a}") // 14.0

a = 7.0
a /= 2.0
println("Double: a /= 2.0: ${a}") // 3.5

a = 7.0
a %= 2.0
println("Double: a %= 2.0: ${a}") // 1.0
